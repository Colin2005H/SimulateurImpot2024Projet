package com.kerware.simulateur;

/**
 * Enumération représentant les différentes situations familiales possibles
 * pour un contribuable lors de la déclaration de ses revenus.
 */
public enum SituationFamiliale {
    /**
     * Contribuable célibataire.
     */
    CELIBATAIRE,

    /**
     * Contribuable pacsé.
     */
    PACSE,

    /**
     * Contribuable marié.
     */
    MARIE,

    /**
     * Contribuable divorcé.
     */
    DIVORCE,

    /**
     * Contribuable veuf.
     */
    VEUF
}
